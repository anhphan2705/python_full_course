import math

pi = 3.14
x = 1
y = 2
z = 3

#round(value) = Return rounded number
print(round(pi))

#math.ceil(value) = Return rounded number up to the closest Integer
print(math.ceil(pi))

#math.floor(value) = Return rounded number down to the closest Integer
print(math.floor(pi))

#abs(value) = return absolute value
print(abs(pi))

#pow(base, power) = return result of base to the power
print(pow(pi, 2))

#math.sqrt(value) = return a squareroot value 
print(math.sqrt(pi))

#max(val1, val2, ...) = return the highest value of all the value passed in
print(max(x, y, z))

#min(val1, val2, ...) = return the lowest value of all the value passed in
print(min(x, y, z))
