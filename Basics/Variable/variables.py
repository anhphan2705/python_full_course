#Variables: a containter for a value. Behaves as the value that is contains

#String
first_name = "Anh"
last_name = "Phan"
full_name = first_name + " " + last_name
print(full_name)
print("Anh P")

#int
age = 18
age = 18+1
age -= 1
print(age)

#float
height = 181.5
print(height)
print("Your height is " + height)

#boolean
isHuman = True
print(isHuman)

#Check data type:
print(type(full_name))
print(type(age))
print(type(height))
print(type(isHuman))

#Convert data type
print(str(age))
print(int(age))
