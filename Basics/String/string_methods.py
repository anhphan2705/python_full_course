name = "anh"

#len(String) = return length of the String
print(len(name))

#String.find(Char) = return the first index for Char
print(name.find("n"))

#String.capitalize() = return String with capitalized firsst letter
print(name.capitalize())

#String.upper() = return String in all uppercase
print(name.upper())

#String.lower() = return String in all lower case
print(name.lower())

#String.isdigit() = return True/False depending on if String contains only digits numbers
print(name.isdigit())

#String.isalpha() = return True/Flase depending on if the String contains only alphabetical letters
print(name.isalpha())

#String.count(Char) = return how many times Char appears in the String
print(name.count("an"))

#String.replace(to_be_replaced, replace_with_this) = return String with desired replacement
print(name.replace("a", "A"))

#String*num = Return String num of times
print(name*3)